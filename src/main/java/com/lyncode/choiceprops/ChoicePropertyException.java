package com.lyncode.choiceprops;

public class ChoicePropertyException extends Exception {
	private static final long serialVersionUID = 3653227864084765326L;

	public ChoicePropertyException() {
	}

	public ChoicePropertyException(String arg0) {
		super(arg0);
	}

	public ChoicePropertyException(Throwable arg0) {
		super(arg0);
	}

	public ChoicePropertyException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
